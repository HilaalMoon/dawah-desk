// Stamps the current time into src/buildInfo.ts as the "last updated" marker
// shown in the app's sidebar (Production Version section).
//
// Run this when cutting a release, BEFORE the release commit:
//   npm run stamp
//
// Why a committed file: the app ships as source and runs via the Vite dev
// server on each user's machine (no build step, no git there), so the value
// must travel inside the release zip. Never wire this into dev/start scripts —
// it must only run on the maintainer's machine at release time, otherwise it
// would reset to each user's app-open time (the original bug).

import { writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const outFile = path.join(root, "src", "buildInfo.ts");
const iso = new Date().toISOString();

const contents = `// Auto-generated by scripts/stamp-build-info.mjs — do not edit by hand.
// Run \`npm run stamp\` when cutting a release to refresh this value.
export const LAST_UPDATED = ${JSON.stringify(iso)};
`;

writeFileSync(outFile, contents, "utf8");
console.log("Stamped src/buildInfo.ts  LAST_UPDATED =", iso);
